package MathUtils;

public class MathUtils {
    private int sayi1;
    private int sayi2;
    private int sayi3;

    public static int karesiniAl(int sayi1) {
        return sayi1 * sayi1;
    }

    public static int sayiyiBesIleCarp(int sayi1) {
        return 5 * sayi1;
    }

    public static double ortalamasiniAl(int sayi1, int sayi2, int sayi3) {
        return (sayi1 + sayi2 + sayi3) / 3;
    }
}
